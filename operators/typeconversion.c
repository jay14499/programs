#include<stdio.h>
int main()
{
 int x=20, y=3;
 float z;

 printf("x+y=%f\n",  z=x+y);

 printf("x-y=%f\n", z=x-y);

 printf("x*y=%f\n", z=x*y);

 printf("x/y=%f\n", z=x/y);
 
 printf("after type conversion\n");
 
 printf("x+y=%f\n",  z=(float)x+(float)y);

 printf("x-y=%f\n", z=(float)x-(float)y);

 printf("x*y=%f\n", z=(float)x*(float)y);

 printf("x/y=%f\n", z=(float)x/(float)y);
 
 }
